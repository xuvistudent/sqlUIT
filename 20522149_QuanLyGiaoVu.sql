CONSTRAINT FK_MALOP FOREIGN KEY (MALOP) REFERENCES LOP(MALOP),
CONSTRAINT FK_MAMH_GIANGDAY FOREIGN KEY (MAMH) REFERENCES MONHOC(MAMH)
)

